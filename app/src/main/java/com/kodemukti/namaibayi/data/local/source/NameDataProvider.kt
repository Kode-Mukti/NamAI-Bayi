package com.kodemukti.namaibayi.data.local.source

data class LocalName(
    val name: String,
    val meaning: String,
    val origin: String,
    val gender: String,
    val pronunciation: String,
    val culturalContext: String,
    val alternativeSpellings: List<String> = emptyList(),
    val popularityRank: Int = 999,
    val strategyTag: String = "modern_trendy",
)

object NameDataProvider {

    fun getNames(): List<LocalName> = allNames

    fun search(query: String): List<LocalName> = allNames.filter {
        it.name.contains(query, ignoreCase = true) ||
        it.meaning.contains(query, ignoreCase = true) ||
        it.origin.contains(query, ignoreCase = true)
    }

    fun recommend(gender: String, strategy: String? = null, count: Int = 10): List<LocalName> {
        val pool = allNames.filter { it.gender == gender || gender == "neutral" }
        val filtered = if (strategy != null) pool.filter { it.strategyTag == strategy } else pool
        return filtered.shuffled().take(count).sortedBy { it.popularityRank }
    }
}

